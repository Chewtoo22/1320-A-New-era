def calculate_effective_stats(car_data, upgrades):
    base_et = car_data["quarterMile"]
    base_speed = car_data["trapSpeed"]
    base_hp = car_data["hp"]

    et_reduction = 0
    speed_increase = 0
    hp_bonus = 0

    for part_key, tier in upgrades.items():
        if tier is None or part_key not in PARTS_CATALOG:
            continue
        part = PARTS_CATALOG[part_key]["tiers"].get(tier)
        if part:
            et_reduction += part["et_reduction"]
            speed_increase += part["speed_increase"]
            hp_bonus += part["hp_bonus"]

    return {
        "effectiveET": round(max(base_et - et_reduction, 5.0), 2),
        "effectiveSpeed": round(base_speed + speed_increase, 1),
        "effectiveHP": base_hp + hp_bonus,
        "etReduction": round(et_reduction, 2),
        "speedIncrease": round(speed_increase, 1),
        "hpBonus": hp_bonus,
    }
